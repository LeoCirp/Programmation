-- TABLE  EMP

CREATE TABLE EMP
     (EMPNO INTEGER(4) UNSIGNED NOT NULL,
	ENAME VARCHAR(10) NOT NULL,
	JOB VARCHAR(9) NOT NULL,
	MGR INTEGER (4),
	HIREDATE DATE,
	SAL DECIMAL(7,2) NOT NULL,
	COMM decimal(7,2),
	DEPTNO integer(10) unsigned NOT NULL,
PRIMARY KEY (EMPNO),
CONSTRAINT deptno_fk FOREIGN KEY (DEPTNO) REFERENCES DEPT(DEPTNO) ON DELETE RESTRICT ON UPDATE CASCADE
